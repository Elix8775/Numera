from Numera.Math import Math

print(Math.ln(2.718))          # ≈ 1.0
print(Math.log(1000))          # 3.0
print(Math.factorial(10))      # 3628800
print(Math.C(10, 3))           # 120
print(Math.P(5, 2))            # 20
print(Math.cumsum([1,2,3,4]))  # [1, 3, 6, 10]
print(Math.exp([0, 1, 2]))     # [1.0, 2.718, 7.389]